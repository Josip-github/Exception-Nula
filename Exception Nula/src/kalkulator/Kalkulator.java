package kalkulator;

public class Kalkulator {

	int broj1, broj2;
	
	public static void zbrajanje(int broj1, int broj2) {
		System.out.println(broj1 + broj2);
	}
	
	public static void oduzimanje(int broj1, int broj2) {
		System.out.println(broj1 - broj2);
	}
	
	public static void množenje(int broj1, int broj2) {
		System.out.println(broj1 * broj2);
	}
	
	public static void dijeljenje(int broj1, int broj2) {
		System.out.println(broj1 / broj2);
	}
}
